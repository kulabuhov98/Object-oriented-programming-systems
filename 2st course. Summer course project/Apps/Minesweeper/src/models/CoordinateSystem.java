package models;

public class CoordinateSystem {
    /* Значение координаты по оси X */
    public int x;

    /* Значение координаты по оси Y */
    public int y;

    /* Конструктор класса CoordinateSystem */
    public CoordinateSystem(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
